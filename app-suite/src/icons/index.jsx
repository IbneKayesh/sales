/* ========================================
   ERP Suite — Centralized SVG Icon Library
   All icons accept: size (default 24), className, and spread props
   Most icons use currentColor for fill/stroke
   ======================================== */

function withDefaults(Component) {
  return function IconWrapper({
    size = 24,
    width,
    height,
    className,
    ...props
  }) {
    return (
      <Component
        width={width ?? size}
        height={height ?? size}
        className={className}
        {...props}
      />
    );
  };
}

